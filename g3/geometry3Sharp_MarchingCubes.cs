	public void Generate()
	{
		Mesh = new DMesh3();
		VertexHash = new Dictionary<Vector3d, int>();
		int x = (int)(Bounds.Width / CubeSize) + 1;
		int y = (int)(Bounds.Height / CubeSize) + 1;
		int z = (int)(Bounds.Depth / CubeSize) + 1;
		CellDimensions = new Vector3i(x, y, z);
		if (ParallelCompute)
		{
			generate_parallel();
		}
		else
		{
			generate_basic();
		}
	}

	private void GridToPos(int x, int y, int z, ref Vector3d p)
	{
		p.x = Bounds.Min.x + CubeSize * (double)x;
		p.y = Bounds.Min.y + CubeSize * (double)y;
		p.z = Bounds.Min.z + CubeSize * (double)z;
	}

