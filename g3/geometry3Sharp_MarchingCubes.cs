		cell.p[7].y = y2;
		cell.p[7].z = z2;
		for (int i = 0; i < 8; i++)
		{
			cell.f[i] = Implicit.Value(ref cell.p[i]);
		}
	}

	private void shift_cell_x(GridCell cell, int xi)
	{
		double x = cell.p[1].x;
		double x2 = Bounds.Min.x + CubeSize * (double)(xi + 1);
		cell.p[0].x = x;
		cell.p[1].x = x2;
		cell.p[2].x = x2;
		cell.p[3].x = x;
		cell.p[4].x = x;
		cell.p[5].x = x2;
		cell.p[6].x = x2;
		cell.p[7].x = x;
		cell.f[0] = cell.f[1];
		cell.f[3] = cell.f[2];
		cell.f[4] = cell.f[5];
		cell.f[7] = cell.f[6];
		cell.f[1] = Implicit.Value(ref cell.p[1]);
		cell.f[2] = Implicit.Value(ref cell.p[2]);
		cell.f[5] = Implicit.Value(ref cell.p[5]);
		cell.f[6] = Implicit.Value(ref cell.p[6]);
	}

	private void generate_parallel()
	{
		hash_lock = default(SpinLock);
		mesh_lock = default(SpinLock);
		bParallel = true;
		gParallel.ForEach(Interval1i.Range(CellDimensions.z), delegate(int zi)
		{
			GridCell cell = new GridCell();
			Vector3d[] vertList = new Vector3d[12];
			for (int i = 0; i < CellDimensions.y; i++)
			{
				if (CancelF())
				{
					break;
				}
				Vector3i idx = new Vector3i(0, i, zi);
				initialize_cell(cell, ref idx);
				polygonize_cell(cell, vertList);
				for (int j = 1; j < CellDimensions.x; j++)
				{
					shift_cell_x(cell, j);
					polygonize_cell(cell, vertList);
				}
			}
		});
		bParallel = false;
	}

	private void generate_basic()
	{
		GridCell cell = new GridCell();
		Vector3d[] vertList = new Vector3d[12];
		for (int i = 0; i < CellDimensions.z; i++)
		{
			for (int j = 0; j < CellDimensions.y; j++)
			{
				if (CancelF())
				{
					return;
				}
				Vector3i idx = new Vector3i(0, j, i);
				initialize_cell(cell, ref idx);
				polygonize_cell(cell, vertList);
				for (int k = 1; k < CellDimensions.x; k++)
				{
					shift_cell_x(cell, k);
					polygonize_cell(cell, vertList);
				}
			}
		}
	}

	private bool polygonize_cell(GridCell cell, Vector3d[] vertList)
	{
		int num = 0;
		int num2 = 1;
		for (int i = 0; i < 8; i++)
		{
			if (cell.f[i] < IsoValue)
			{
				num |= num2;
			}
			num2 <<= 1;
		}
		if (edgeTable[num] == 0)
		{
			return false;
		}
		num2 = 1;
		for (int j = 0; j <= 11; j++)
		{
			if ((edgeTable[num] & num2) != 0)
			{
				int num3 = edge_indices[j, 0];
				int num4 = edge_indices[j, 1];
				find_iso(ref cell.p[num3], ref cell.p[num4], cell.f[num3], cell.f[num4], ref vertList[j]);
			}
			num2 <<= 1;
		}
		int num5 = 0;
		for (int k = 0; triTable[num, k] != -1; k += 3)
		{
			int num6 = triTable[num, k];
			int num7 = find_or_append_vertex(ref vertList[num6]);
			int num8 = triTable[num, k + 1];
			int num9 = find_or_append_vertex(ref vertList[num8]);
			int num10 = triTable[num, k + 2];
			int num11 = find_or_append_vertex(ref vertList[num10]);
			if (num7 != num9 && num7 != num11 && num9 != num11)
			{
				append_triangle(num7, num9, num11);
				num5++;
			}
		}
		return num5 > 0;
	}

	private int find_or_append_vertex(ref Vector3d pos)
	{
		bool lockTaken = false;
		if (bParallel)
		{
			hash_lock.Enter(ref lockTaken);
		}
		if (!VertexHash.TryGetValue(pos, out var value))
		{
			value = append_vertex(pos);
			VertexHash[pos] = value;
		}
		if (lockTaken)
		{
			hash_lock.Exit();
		}
		return value;
	}

	private int append_vertex(Vector3d v)
	{
		bool lockTaken = false;
		if (bParallel)
		{
			mesh_lock.Enter(ref lockTaken);
		}
		int result = Mesh.AppendVertex(v);
		if (lockTaken)
		{
			mesh_lock.Exit();
		}
		return result;
	}

	private int append_triangle(int a, int b, int c)
	{
		bool lockTaken = false;
		if (bParallel)
		{
			mesh_lock.Enter(ref lockTaken);
		}
		int result = Mesh.AppendTriangle(a, b, c);
		if (lockTaken)
		{
			mesh_lock.Exit();
		}
		return result;
	}

	private void find_iso(ref Vector3d p1, ref Vector3d p2, double valp1, double valp2, ref Vector3d pIso)
	{
		if (Math.Abs(valp1 - valp2) < 1E-05)
		{
			pIso = (p1 + p2) * 0.5;
			return;
		}
		if (Math.Abs(IsoValue - valp1) < 1E-05)
		{
			pIso = 0.999999 * p1 + 1.0000000000287557E-06 * p2;
			return;
		}
		if (Math.Abs(IsoValue - valp2) < 1E-05)
		{
			pIso = 0.999999 * p2 + 1.0000000000287557E-06 * p1;
			return;
		}
		Vector3d a = p1;
		Vector3d b = p2;
		double num = valp1;
		double num2 = valp2;
		if (valp2 < valp1)
		{
			a = p2;
			b = p1;
			num2 = valp1;
			num = valp2;
		}
		if (RootMode == RootfindingModes.Bisection)
		{
			for (int i = 0; i < RootModeSteps; i++)
			{
				pIso.x = (a.x + b.x) * 0.5;
				pIso.y = (a.y + b.y) * 0.5;
				pIso.z = (a.z + b.z) * 0.5;
				double num3 = Implicit.Value(ref pIso);
				if (num3 < IsoValue)
				{
					a = pIso;
					num = num3;
				}
				else
				{
					b = pIso;
					num2 = num3;
				}
			}
			pIso = Vector3d.Lerp(a, b, 0.5);
			return;
		}
		double num4 = 0.0;
		if (RootMode == RootfindingModes.LerpSteps)
		{
			for (int j = 0; j < RootModeSteps; j++)
			{
				num4 = (IsoValue - num) / (num2 - num);
				pIso.x = a.x + num4 * (b.x - a.x);
				pIso.y = a.y + num4 * (b.y - a.y);
				pIso.z = a.z + num4 * (b.z - a.z);
				double num5 = Implicit.Value(ref pIso);
				if (num5 < IsoValue)
				{
					a = pIso;
					num = num5;
				}
				else
				{
					b = pIso;
					num2 = num5;
				}
			}
		}
		num4 = (IsoValue - num) / (num2 - num);
		pIso.x = a.x + num4 * (b.x - a.x);
		pIso.y = a.y + num4 * (b.y - a.y);
		pIso.z = a.z + num4 * (b.z - a.z);
	}
